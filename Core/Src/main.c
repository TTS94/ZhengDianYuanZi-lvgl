void lvglTask1ms(void const * argument){
  int32_t cnt = 0;

  while(1){
    lv_tick_inc(1); // 告诉LVGL：时间过了1ms
    osDelay(1);     // 延时1ms
    cnt++;
    cnt %= 1000;
    if(cnt == 0){
      HAL_GPIO_TogglePin(LED0_GPIO_Port, LED0_Pin);
    }
  }
}

